package patterns.facade;

public class MusicSystem {

    /**
     * Включает музыку.
     */
    public void playMusic() {
        System.out.println("Музыка играет...");
    }

    /**
     * Выключает музыку.
     */
    public void stopMusic() {
        System.out.println("Музыка остановилась...");
    }

}
